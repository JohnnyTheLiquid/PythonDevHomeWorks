tutors = [
    'Иван', 'Анастасия', 'Петр', 'Сергей',
    'Дмитрий', 'Борис', 'Елена'
]
klasses = [
    '9А', '7В', '9Б', '9В', '8Б', '10А', '10Б', '9А'
]

# генерируем кортежи
pairs_gen = ((i,j) for i,j in zip(tutors, klasses))

# проверяем тип результата - д.б. генератор и выводим кортежи
print(type(pairs_gen))

for i in pairs_gen:
    print(i)
